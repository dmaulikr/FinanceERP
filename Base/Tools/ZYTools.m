+ (NSString*)getDirectoryForDocuments:(NSString*)dir
{
    NSArray *array =  NSSearchPathForDirectoriesInDomains(NSDocumentDirectory, NSUserDomainMask, YES);
    NSString *documentPath = [array objectAtIndex:0];
    NSString* dirPath = [documentPath stringByAppendingPathComponent:dir];
    BOOL isDir = NO;
    BOOL isCreated = [[NSFileManager defaultManager] fileExistsAtPath:dirPath isDirectory:&isDir];
    if (isCreated == NO || isDir == NO) {
        NSError* error = nil;
        BOOL success = [[NSFileManager defaultManager] createDirectoryAtPath:dirPath withIntermediateDirectories:YES attributes:nil error:&error];
        if (success == NO)
            NSLog(@"create dir error: %@", error.debugDescription);
    }
    return dirPath;
}
+ (BOOL)clearCache
{
    NSArray *documentArray =  NSSearchPathForDirectoriesInDomains(NSDocumentDirectory, NSUserDomainMask, YES);
    NSString *documentPath = [documentArray objectAtIndex:0];
    NSArray *libraryArray =  NSSearchPathForDirectoriesInDomains(NSLibraryDirectory, NSUserDomainMask, YES);
    NSString *libraryPath = [libraryArray objectAtIndex:0];
    
    NSFileManager *manager = [NSFileManager defaultManager];
    NSError *documentPathError = nil;
    NSError *libraryPathError = nil;
    [manager removeItemAtPath:documentPath error:&documentPathError];
    [manager removeItemAtPath:libraryPath error:&documentPathError];
    return documentPathError==nil&&libraryPathError;
}
